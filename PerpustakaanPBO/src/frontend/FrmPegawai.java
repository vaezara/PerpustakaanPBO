    // Method untuk menampilkan data pegawai ke tabel
        public void tampilkanData() {
            String[] kolom = {"ID", "Nama", "Alamat", "Telepon", "Jabatan", "Username", "Password"}; // Tambahkan kolom baru
            ArrayList<Pegawai> list = new Pegawai().getAll(); // Mengambil data semua pegawai
            Object rowData[] = new Object[7]; // Sesuaikan jumlah kolom data

        // Membuat model tabel dengan kolom yang sesuai
        DefaultTableModel model = new DefaultTableModel(new Object[][]{}, kolom);
        tblPegawai.setModel(model);

        // Mengisi tabel dengan data pegawai
        for (Pegawai pegawai : list) {
            rowData[0] = pegawai.getIdpegawai();  // ID Pegawai
            rowData[1] = pegawai.getNama();       // Nama
            rowData[2] = pegawai.getAlamat();     // Alamat
            rowData[3] = pegawai.getNotelp();     // Telepon
            rowData[4] = pegawai.getJabatan();    // Jabatan
            rowData[5] = pegawai.getUsername();   // Username
            rowData[6] = pegawai.getPassword();   // Password

            // Menambahkan baris ke model tabel
            ((DefaultTableModel) tblPegawai.getModel()).addRow(rowData);
        }
    }


       // Method untuk mencari data pegawai berdasarkan keyword
public void cari(String keyword) {
    String[] kolom = {"ID", "Nama", "Alamat", "Telepon", "Jabatan", "Username", "Password"}; // Kolom tabel
    ArrayList<Pegawai> list = new Pegawai().search(keyword); // Mencari data sesuai keyword
    Object rowData[] = new Object[7]; // Sesuaikan jumlah kolom data

    // Membuat model tabel dengan kolom yang sesuai
    DefaultTableModel model = new DefaultTableModel(new Object[][]{}, kolom);
    tblPegawai.setModel(model);

    // Mengisi tabel dengan hasil pencarian
    for (Pegawai pegawai : list) {
        rowData[0] = pegawai.getIdpegawai();  // ID Pegawai
        rowData[1] = pegawai.getNama();       // Nama
        rowData[2] = pegawai.getAlamat();     // Alamat
        rowData[3] = pegawai.getNotelp();     // Telepon
        rowData[4] = pegawai.getJabatan();    // Jabatan
        rowData[5] = pegawai.getUsername();   // Username
        rowData[6] = pegawai.getPassword();   // Password

        // Menambahkan baris ke model tabel
         ((DefaultTableModel) tblPegawai.getModel()).addRow(rowData);
    }
}


