    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        Pegawai pgw = new Pegawai();
        pgw.setIdpegawai(Integer.parseInt(txtidPegawai.getText())); // Mengatur ID Pegawai
        pgw.setNama(txtNama.getText());                            // Mengatur Nama
        pgw.setAlamat(txtAlamat.getText());                        // Mengatur Alamat
        pgw.setNotelp(txtNoTelp.getText());                        // Mengatur No Telepon
        pgw.setJabatan(txtJabatan.getText());                      // Mengatur Jabatan
        pgw.setUsername(txtUsername.getText());                   // Mengatur Username
        pgw.setPassword(txtPassword.getText());                    // Mengatur Password

        pgw.save(); // Menyimpan data ke database

        // Menampilkan ID Pegawai yang baru disimpan
        txtidPegawai.setText(Integer.toString(pgw.getIdpegawai()));

        // Memperbarui tabel untuk menampilkan data terbaru
        tampilkanData();

         // TODO add your handling code here:
    }//GEN-LAST:event_btnSimpanActionPerformed

